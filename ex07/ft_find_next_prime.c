int	ft_find_next_prime(int nb)
{
	unsigned int a;

	if (nb < 3)
		return (2);
	if (!(nb % 2))
		nb++;
	a = 3;
	while (a * a <= (unsigned int)nb)
	{
		if (!(nb % a))
		{
			nb += 2;
			a = 1;
		}
		a += 2;
	}
	return (nb);
}
